#include "Task.h"

//==========================================
// コンストラクタ
//==========================================
Task::Task(const std::function<void(void)>& task) :
	func(task), cnt(0), current(State::Idle)
{
}

//==========================================
// デストラクタ
//==========================================
Task::~Task()
{
}

//==========================================
// 停止
//==========================================
void Task::Stop()
{
	current = State::Finished;
}

//==========================================
// 再開
//==========================================
void Task::Resume()
{
	current = State::Idle;
}

//==========================================
// 一時停止
//==========================================
void Task::Pause()
{
	current = State::Paused;
}

//==========================================
// 終了判定
//==========================================
bool Task::IsFinished()
{
	return current = State::Finished;
}
